package com.example;

public class Carte {

    private String nom;
    private String color;
    private int valeur;

    public String getnom() {
        return nom;
    }

    public String getcolor() {
        return color;
    }

    public int getvaleur() {
        return valeur;
    }

    public void setnom(String nom) {
        this.nom = nom;
    }

    public void setcolor(String color) {
        this.color = color;
    }

    public void setvaleur(int value) {
        this.valeur = value;
    }

}
